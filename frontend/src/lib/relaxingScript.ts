const THEME_PACKS: Record<RelaxingTheme, ThemePack> = {
  palace_romance: PALACE,
  ceo_contract: CEO,
  rebirth_revenge: REBIRTH,
  campus_sweet: CAMPUS,
};

export const THEME_OPTIONS: { value: RelaxingTheme; label: string }[] = [
  { value: 'palace_romance', label: '🏯 รักในวังจีนโบราณ' },
  { value: 'ceo_contract', label: '💼 สัญญารักซีอีโอเย็นชา' },
  { value: 'rebirth_revenge', label: '🔥 เกิดใหม่ แค้นรัก' },
  { value: 'campus_sweet', label: '🌸 รักใสวัยเรียน' },
];

// จำนวนฉาก "ตึงเครียด/ยั่วเย้า" ที่จะแทรกตามความยาว
const LENGTH_SETTINGS: Record<ScriptLength, { tension: number; extraMeet: number }> = {
  short: { tension: 1, extraMeet: 0 },
  medium: { tension: 2, extraMeet: 1 },
  long: { tension: 3, extraMeet: 1 },
};

/**
 * ปั่นบทพากย์ 1 ชุด — สุ่มฉากและประโยคทุกครั้ง จึงไม่ซ้ำเดิม
 */
export function generateRelaxingScript(opts: GenerateOptions): GeneratedScript {
  const pack = THEME_PACKS[opts.theme] || PALACE;
  const male = opts.maleName?.trim() || pick(MALE_NAMES);
  const female = opts.femaleName?.trim() || pick(FEMALE_NAMES);
  const cfg = LENGTH_SETTINGS[opts.length] || LENGTH_SETTINGS.medium;

  // เลือกคลังฉากยั่วเย้าตามระดับความเผ็ด (medium = ผสมทั้งสองคลัง)
  const tensionPool =
    opts.spice === 'medium'
      ? shuffle([...pack.tension, ...pack.tensionSpicy])
      : shuffle([...pack.tension]);

  const lines: string[] = [];
  lines.push(pick(pack.hook));
  lines.push(pick(pack.meet));
  // ฉากพบกันเสริม (ความยาวกลาง/ยาว) — หยิบอีกแบบที่ไม่ซ้ำกับอันแรก
  for (const extra of sampleUnique(pack.meet, cfg.extraMeet)) {
    if (!lines.includes(extra)) lines.push(extra);
  }
  // ฉากยั่วเย้า/ตึงเครียด
  for (const t of tensionPool.slice(0, cfg.tension)) lines.push(t);
  lines.push(pick(pack.conflict));
  lines.push(pick(pack.turn));
  lines.push(pick(pack.climax));
  lines.push(pick(pack.cliffhanger));

  const substitute = (s: string) =>
    s.replace(/\{ช\}/g, male).replace(/\{ญ\}/g, female);

  const text = lines.map(substitute).join('\n');
  const title = `${pick(pack.titles)} — ${female} & ${male}`;

  return {
    title,
    text,
    maleName: male,
    femaleName: female,
    beatCount: lines.length,
  };
}
