// ── ยูทิล ────────────────────────────────────────────────────────────────────
const pick = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];

const shuffle = <T,>(arr: T[]): T[] => {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
};

/** สุ่มหยิบ n รายการแบบไม่ซ้ำจาก pool (ถ้า pool เล็กกว่า n ก็คืนเท่าที่มี) */
const sampleUnique = <T,>(arr: T[], n: number): T[] => shuffle(arr).slice(0, Math.max(0, n));

