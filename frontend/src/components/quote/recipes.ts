// ── คลัง "สูตรคอนเทนต์" (Content Recipe) ──
// สูตร 1 อัน = แนวการเขียน/ทำนาย + เลย์เอาต์ที่จับคู่ + คำสั่ง AI เฉพาะทาง
//             + คลังหัวข้อสำหรับสุ่ม + ตัวอย่างสำเร็จรูป (โชว์ได้ทันทีไม่ต้องรอ AI)
// เพิ่มสูตรใหม่ทีหลังได้เลย — แค่ push ลง RECIPES โดยอ้าง templateId ที่มีอยู่ใน templates.ts

import type { CategoryId } from './types';

export interface RecipeSample {
  title?: string;          // หัวข้อ/พาดหัว (เฉพาะสูตรที่ hasTitle)
  blocks: string[];        // เนื้อหาแต่ละบล็อก (ใช้ \n ขึ้นบรรทัด)
}

export interface ContentRecipe {
  id: string;
  category: CategoryId;
  name: string;
  icon: string;
  hook: string;            // คำอธิบายสั้นๆ ว่าสูตรนี้คืออะไร / ทำไมปัง
  templateId: string;      // เลย์เอาต์เริ่มต้นที่จับคู่ (อ้างอิง BUILT_IN_TEMPLATES)
  blockCount: number;      // จำนวนบล็อกเนื้อหาที่ต้องเขียน (= จำนวน text-block ของเทมเพลต)
  hasTitle: boolean;       // true = มีหัวข้อ (map ลง title_main), false = มีแต่บล็อก
  badges?: string[];       // ป้าย A/B/C/D หรือ 1/2/3/4 หรือ A/B/AB/O (เฉพาะเทมเพลต icon_list_4)
  badgeColors?: string[];  // สีพื้นป้ายแต่ละอัน (ทับสีเริ่มต้นของเทมเพลต) — เช่น โทนแดงเลือด
  cellColors?: string[];   // สีพื้นการ์ดแต่ละช่อง block_i (เช่น สีมงคลประจำวันเกิด)
  labels?: string[];       // ป้ายชื่อตายตัวต่อช่อง (เช่น 12 ราศี / 7 วัน) — AI เขียนแค่เนื้อหา ระบบเติมชื่อให้ กันช่องเลื่อน/รวมกัน
  structure: string;       // คำสั่ง AI: แต่ละบล็อกควรมีอะไร (ฉีดเข้า system prompt)
  topicPool: string[];     // คลังหัวข้อ — ใช้เป็นตัวเลือก + สุ่ม
  sample: RecipeSample;    // ตัวอย่างสำเร็จรูป
}

